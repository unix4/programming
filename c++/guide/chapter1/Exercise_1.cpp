/*
	Write a C++ program that outputs the following text on screen:
	
	Oh what
	a happy day!
	Oh yes,
	what a happy day!
	
	Use the manipulator endl where appropriate.
*/

#include <iostream>

int main()
{
	std::cout << "Oh what" << std::endl;
	std::cout << "a happy day!" << std::endl;
	std::cout << "Oh yes," << std::endl;
	std::cout << "what a happy day!" << std::endl;
	
	return 0;
}
